// This add function adds two numbers
function add(a, b) {
  return a + b; // Returns aggregation of a and b
}

/* 
Below function is a multiply function.
a is number and b is number
*/
function multiply(a, b) {
  return a * b;
}

/**
 *
 * @param {number} a - parameter holds a number
 * @param {number} b - parameter holds a number
 * @returns {number} - return division of two numbers
 * @example
 * // returns 2
 * division(4, 2);
 */
function division(a, b) {
  return a / b;
}
